#include <Cartridge.h>
#include <stdexcept>

static constexpr uint8_t nintendoLogo[48] = {
	0xCE,0xED,0x66,0x66,0xCC,0x0D,0x00,0x0B,
	0x03,0x73,0x00,0x83,0x00,0x0C,0x00,0x0D,
	0x00,0x08,0x11,0x1F,0x88,0x89,0x00,0x0E,
	0xDC,0xCC,0x6E,0xE6,0xDD,0xDD,0xD9,0x99,
	0xBB,0xBB,0x67,0x63,0x6E,0x0E,0xEC,0xCC,
	0xDD,0xDC,0x99,0x9F,0xBB,0xB9,0x33,0x3E
};

Cartridge::Cartridge()
{
	bank0.fill(0);
	bank1.fill(0);

	// read in the nintendo logo into the header.
	for (int i = 0; i < 48; i++)
	{
		bank0[0x0104 + i] = nintendoLogo[i];
	}
}

uint8_t Cartridge::GetByte(uint16_t addr)
{
	if (addr < 0x4000)
		return bank0[addr];
	else if (addr < 0x8000)
		return bank1[addr-0x4000];
	else
		throw new std::runtime_error("[ERROR]: Trying to access outside of Cartridge memory.");
}

void Cartridge::WriteByte(uint16_t addr, uint8_t data)
{
	if (addr < 0x4000)
		bank0[addr] = data;
	else if (addr < 0x8000)
		bank1[addr] = data;
	else
		throw new std::runtime_error("[ERROR]: Trying to access outside of Cartridge memory.");
}